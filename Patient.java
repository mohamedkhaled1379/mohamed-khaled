package com.mycompany.dentalclinic;

/**
 *
 * @author acer
 */
public class Patient {
    private String name;
    private String email;
    private String mobile;

    public Patient(String name, String email, String mobile) {
        this.name = name;
        this.email = email;
        this.mobile = mobile;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getMobile() {
        return mobile;
    }
}
